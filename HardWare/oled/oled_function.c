//初始化OLED的驱动SSD1306
void oled_init(void)
{
	//给OLED发送命令 初始化OLED
		WriteOLedCmd(0xAE);//--turn off oled panel关闭显示
		WriteOLedCmd(0x00);//---set low column address设置列起始地址低4位
		WriteOLedCmd(0x10);//---set high column address设置列起始地址高4位
		WriteOLedCmd(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)设置显示开始0-63
		WriteOLedCmd(0x81);//--set contrast control register 对比度
		WriteOLedCmd(0xCF);//--Set SEG Output Current Brightness
		WriteOLedCmd(0xA1);//--Set SEG/Column Mapping(A0h/A1h) 设置Segment重映射
		WriteOLedCmd(0xC8);//--Set COM/Row Scan Direction(C0h/C8h) 设置COM输出扫描方向
		WriteOLedCmd(0xA6);//--set normal display
		WriteOLedCmd(0xA8);//--set multiplex ratio(1 to 64)设置分辨率
		WriteOLedCmd(0x3f);//--128*64个像素大小；0x1f：128*32是0.91寸大小的OLED
		WriteOLedCmd(0xD3);//--set display offset设置显示偏移
		WriteOLedCmd(0x00);//--not offset无偏移
		WriteOLedCmd(0xD5);//--set display clock divide ratio/oscillator frequency设置显示时钟分频率、振荡器频率
		WriteOLedCmd(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
		WriteOLedCmd(0xD9);//--set pre-charge period设置重充电周期
		WriteOLedCmd(0xF1);//--Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
		WriteOLedCmd(0xDA);//--set com pins hardware configuration设置COM引脚硬件配置
		WriteOLedCmd(0x12);//0.96寸OLEDCOM引脚硬件配置
		WriteOLedCmd(0xDB);//--set vcomh设置Vcomh取消选择水平
		WriteOLedCmd(0x40);//--Set VCOM Deselect Level
		WriteOLedCmd(0x20);//--Set Page Addressing Mode (0x00水平/0x01垂直/0x02页)设置内存地址模式
		WriteOLedCmd(0x02);//页寻址
		WriteOLedCmd(0x8D);//--set Charge Pump enable/disable使能/禁用电荷泵
		WriteOLedCmd(0x14);//--set(0x10) disable
		WriteOLedCmd(0xA4);//--Disable Entire Display On (0xa4/0xa5)全部显示开启
		WriteOLedCmd(0xA6);//--Disable Inverse Display On (0xa6/0xa7)设置正常/反相显示
		WriteOLedCmd(0xAF);//--turn on oled panel开启显示
		
		OLed_Fill(0x00);    //清屏
}